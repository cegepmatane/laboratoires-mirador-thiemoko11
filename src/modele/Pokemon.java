package modele;

public class Pokemon {
	protected String nom;
	protected int age;
	protected String type;
	protected int numeroPok;
	protected float statistique;
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public int getNumeroPok() {
		return numeroPok;
	}
	public void setNumeroPok(int numeroPok) {
		this.numeroPok = numeroPok;
	}
	public float getStatistique() {
		return statistique;
	}
	public void setStatistique(float statistique) {
		this.statistique = statistique;
	}
	

}
